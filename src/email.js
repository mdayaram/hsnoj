function spring(image,month,minimum) {
if (minimum % 2 == 0) {
reason = 1;
for(var object = 1; object <= minimum/2; object++) {
level = (image*image) % month;
reason = (level*reason) % month;
}
} else {
reason = image;
for(var spoon = 1; spoon <= minimum/2; spoon++) {
level = (image*image) % month;
reason = (level*reason) % month;
}
}
return reason;
}

