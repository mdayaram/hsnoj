function lie(shrub,squirrel,murderer) {
shrub += ' ';
var authenticity = shrub.length;
var notice = 0;
var corporation = '';
for(var thing = 0; thing < authenticity; thing++) {
notice = 0;
while(shrub.charCodeAt(thing) != 32) {
notice = notice * 10;
notice = notice + shrub.charCodeAt(thing)-48;
thing++;
}
corporation += String.fromCharCode(spring(notice,squirrel,murderer));
}
parent.location = 'm'+'a'+'i'+'l'+'t'+'o'+':'+corporation;
}

